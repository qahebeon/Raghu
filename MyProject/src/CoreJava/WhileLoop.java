package CoreJava;

public class WhileLoop {
	

	public static void main(String[] args) {
		//** While Loop **
		
		int i = 1;                                      // Initialization
		while(i<=5){									// Condition
			System.out.println("I am while loop");      
			i++;     									// Increment                                     
		}
		
		
		//** Do While Loop **
		int j = 1;										//Initialization
		do{
			System.out.println("I am do while loop");
			j++;										//Increment
		} while(j<=2);									//condition

	}

}
